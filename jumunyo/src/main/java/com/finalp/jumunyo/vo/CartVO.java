package com.finalp.jumunyo.vo;

public class CartVO {
	
	private String cartmenu_id;
	private String cartmenu_name;
	private int cartmenu_price;
	private int cartmenu_count;
	
	
	public String getCartmenu_id() {
		return cartmenu_id;
	}
	public void setCartmenu_id(String cartmenu_id) {
		this.cartmenu_id = cartmenu_id;
	}
	public String getCartmenu_name() {
		return cartmenu_name;
	}
	public void setCartmenu_name(String cartmenu_name) {
		this.cartmenu_name = cartmenu_name;
	}
	public int getCartmenu_price() {
		return cartmenu_price;
	}
	public void setCartmenu_price(int cartmenu_price) {
		this.cartmenu_price = cartmenu_price;
	}
	public int getCartmenu_count() {
		return cartmenu_count;
	}
	public void setCartmenu_count(int cartmenu_count) {
		this.cartmenu_count = cartmenu_count;
	}
	
}
